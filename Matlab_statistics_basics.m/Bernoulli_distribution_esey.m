الخوارزمية
حدد قيمة  p
حدد عدد التجارب n
ولّد أرقام عشوائية
حوّلها إلى 0 و1
احسب التكرارات
استخرج PMF
استخرج CDF
احسب Mean
احسب Variance
%}



%% =========================================================
%  Block 1 : تنظيف البيئة
%  حذف المتغيرات القديمة - تنظيف الشاشة - إغلاق النوافذ
%% =========================================================
clc;
clear;
close all;


%% =========================================================
%  Block 2 : تعريف المتغيرات الأساسية
%  p : احتمال النجاح
%  N : عدد التجارب
%% =========================================================
p = 0.5;      % Probability of success
N = 1000;     % Number of trials


%% =========================================================
%  Block 3 : توليد أرقام عشوائية من 0 إلى 1
%  rand يعطي قيم موزعة بانتظام بين [0,1]
%% =========================================================
r = rand(1,N);


%% =========================================================
%  Block 4 : توليد عينات برنولي
%  إذا كانت القيمة <= p  => نجاح = 1
%  غير ذلك => فشل = 0
%% =========================================================
X = r <= p;


%% =========================================================
%  Block 5 : عرض أول 20 عينة فقط
%  لمشاهدة شكل النتائج
%% =========================================================
disp('First 20 Samples:')
disp(X(1:20))


%% =========================================================
%  Block 6 : حساب عدد النجاحات والفشل
%% =========================================================
count0 = sum(X == 0);   % عدد الأصفار
count1 = sum(X == 1);   % عدد الواحدات


%% =========================================================
%  Block 7 : حساب PMF
%  PMF = احتمال كل قيمة ممكنة
%% =========================================================
pmf0 = count0 / N;   % P(X=0)
pmf1 = count1 / N;   % P(X=1)

fprintf('\nPMF Results:\n');
fprintf('P(X=0) = %.4f\n', pmf0);
fprintf('P(X=1) = %.4f\n', pmf1);


%% =========================================================
%  Block 8 : حساب CDF
%  CDF هو الاحتمال التراكمي
%% =========================================================
x_values = [0 1];

cdf0 = pmf0;            % F(0)
cdf1 = pmf0 + pmf1;    % F(1)

fprintf('\nCDF Results:\n');
fprintf('F(0) = %.4f\n', cdf0);
fprintf('F(1) = %.4f\n', cdf1);


%% =========================================================
%  Block 9 : حساب المتوسط
%  المتوسط النظري لبرنولي = p
%% =========================================================
mean_sim = mean(X);
mean_theoretical = p;

fprintf('\nMean:\n');
fprintf('Simulated Mean = %.4f\n', mean_sim);
fprintf('Theoretical Mean = %.4f\n', mean_theoretical);


%% =========================================================
%  Block 10 : حساب التباين
%  التباين النظري = p(1-p)
%% =========================================================
var_sim = var(X,1);      % Population variance
var_theoretical = p*(1-p);

fprintf('\nVariance:\n');
fprintf('Simulated Variance = %.4f\n', var_sim);
fprintf('Theoretical Variance = %.4f\n', var_theoretical);


%% =========================================================
%  Block 11 : رسم PMF
%% =========================================================
figure;
bar([0 1], [pmf0 pmf1]);
title('Bernoulli PMF');
xlabel('x');
ylabel('P(X=x)');
grid on;


%% =========================================================
%  Block 12 : رسم CDF
%% =========================================================
figure;
stairs(x_values, [cdf0 cdf1], 'LineWidth', 2);
title('Bernoulli CDF');
xlabel('x');
ylabel('F(x)');
grid on;