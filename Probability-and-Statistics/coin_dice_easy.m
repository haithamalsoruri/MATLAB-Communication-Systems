%{
clc; clear;

N = 1000;   % عدد التجارب

countA = 0;
countB = 0;
countC = 0;

for i = 1:N
    
    % رمي العملة
    coin = randi([0 1]);   
    
    % رمي النرد
    dice = randi([1 6]); 
    
    % الحدث A : صورة ورقم زوجي
    if coin==1 && mod(dice,2)==0
        countA = countA + 1;
    end
    
    % الحدث B : كتابة ورقم أكبر من 4
    if coin==0 && dice>4
        countB = countB + 1;
    end
    
    % الحدث C : النرد = 6
    if dice==6
        countC = countC + 1;
    end

end

% الاحتمال التجريبي
PA = countA / N;
PB = countB / N;
PC = countC / N;

% الاحتمال النظري
TA = 3/12;
TB = 2/12;
TC = 2/12;

disp('A  [Theoretical Experimental]')
disp([TA PA])

disp('B  [Theoretical Experimental]')
disp([TB PB])

disp('C  [Theoretical Experimental]')
disp([TC PC])
%}
%% محاكاة تجربة رمي عملة ونرد بمعدل 1000 مرة
% تنظيف بيئة العمل
clc;        % مسح نافذة الأوامر
clear;      % مسح جميع المتغيرات السابقة من الذاكرة

%% إعدادات المحاكاة
N = 1000;   % عدد التجارب (إجمالي الرميات)

% تهيئة العدادات (تبدأ من الصفر)
countA = 0; % عداد الحدث A: صورة ورقم زوجي
countB = 0; % عداد الحدث B: كتابة ورقم أكبر من 4
countC = 0; % عداد الحدث C: ظهور الرقم 6 على النرد

%% بدء حلقة المحاكاة (التكرار)
for i = 1:N
    
    % 1. محاكاة رمي العملة (0 = كتابة، 1 = صورة)
    coin = randi([0 1]);   
    
    % 2. محاكاة رمي النرد (أرقام من 1 إلى 6)
    dice = randi([1 6]); 
    
    % --- فحص الأحداث ---
    
    % الحدث A: فحص إذا كانت العملة صورة (1) والنرد رقم زوجي (باقي القسمة على 2 هو 0)
    if coin == 1 && mod(dice, 2) == 0
        countA = countA + 1;
    end
    
    % الحدث B: فحص إذا كانت العملة كتابة (0) والنرد أكبر من 4 (أي 5 أو 6)
    if coin == 0 && dice > 4
        countB = countB + 1;
    end
    
    % الحدث C: فحص إذا كان النرد أظهر الرقم 6 بغض النظر عن العملة
    if dice == 6
        countC = countC + 1;
    end

end

%% حساب ومقارنة الاحتمالات
% الاحتمال التجريبي = (عدد مرات وقوع الحدث) / (إجمالي التجارب)
PA = countA / N;
PB = countB / N;
PC = countC / N;

% الاحتمال النظري (الحساب الرياضي الدقيق)
% ملاحظة: إجمالي النتائج الممكنة للنرد والعملة معاً هو 12 نتيجة (2 * 6)
TA = 3 / 12; % (صورة+2، صورة+4، صورة+6) = 3 حالات من 12
TB = 2 / 12; % (كتابة+5، كتابة+6) = حالتان من 12
TC = 1 / 6;  % ظهور الرقم 6 هو حالة واحدة من 6 حالات للنرد

%% عرض النتائج النهائية في نافذة الأوامر
disp('----------------------------------------------------')
disp('الحدث  |  الاحتمال النظري  |  الاحتمال التجريبي')
disp('----------------------------------------------------')

fprintf('الحدث A:  %12.4f  |  %12.4f\n', TA, PA);
fprintf('الحدث B:  %12.4f  |  %12.4f\n', TB, PB);
fprintf('الحدث C:  %12.4f  |  %12.4f\n', TC, PC);

disp('----------------------------------------------------')

