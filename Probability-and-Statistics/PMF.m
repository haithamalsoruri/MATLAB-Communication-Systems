% 1. محاكاة رمي النرد (10 مرات مثلاً)
X = randi([1 6], 1, 10); 

% 2. مصفوفة فارغة لتخزين النبضات (النتائج الجديدة)
pulses = zeros(1, 10); 

% 3. الفحص والتحويل باستخدام المنطق (Logic)
for i = 1:10
    if X(i) <= 3
        pulses(i) = -2; % إذا كان الرقم 1، 2، أو 3
    else
        pulses(i) = 2;  % إذا كان الرقم 4، 5، أو 6
    end
end

% 4. الرسم التوضيحي
subplot(2,1,1);
stem(X, 'filled'); title('نتائج النرد الأصلية');
ylim([0 7]); grid on;

subplot(2,1,2);
stairs(pulses, 'LineWidth', 2); title('تمثيل النبضات (-2 و 2)');
ylim([-3 3]); grid on;
