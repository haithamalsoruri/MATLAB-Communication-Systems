clc ;clear all;clf
%% --- الجزء 1: إعداد المتغيرات الأساسية (Initialization) ---
% تحديد عدد مرات تكرار التجربة (كلما زاد العدد زادت دقة النتائج)
n = 10000; 

% توليد عينات عشوائية تمثل رمي النرد (أرقام صحيحة من 1 إلى 6)
% تم استخدام توليد المتجه مباشرة لزيادة سرعة الكود
results = randi([1, 6], 1, n); 


%% --- الجزء 2: حساب دالة الكتلة الاحتمالية (PMF Calculation) ---
% الـ PMF تعطي احتمال ظهور كل وجه من أوجه النرد
pmf = zeros(1, 6); % حجز مساحة لنتائج الاحتمالات

for i = 1:6
    % حساب تكرار الرقم i وقسمته على العدد الكلي للحصول على الاحتمال
    pmf(i) = sum(results == i) / n;
end


%% --- الجزء 3: حساب دالة التوزيع التراكمي (CDF Calculation) ---
% الـ CDF تمثل مجموع الاحتمالات حتى قيمة معينة
cdf = zeros(1, 6);

% القيمة الأولى في التراكمي هي نفس أول قيمة في الـ PMF
cdf(1) = pmf(1); 

% حساب بقية القيم بجمع الاحتمال الحالي مع مجموع ما قبله
for i = 2:6
    cdf(i) = cdf(i-1) + pmf(i);
end


%% --- الجزء 4: تمثيل النتائج بيانياً (Visualization) ---
x = 1:6; % محور السينات يمثل أوجه النرد

% -- رسم الـ PMF --
subplot(2,1,1); % رسم الشكل الأول في الأعلى
stem(x, pmf, 'filled', 'MarkerFaceColor', 'b');
title('دالة الكتلة الاحتمالية (PMF)');
xlabel('وجه النرد');
ylabel('الاحتمال P(X=x)');
grid on;

% -- رسم الـ CDF --
subplot(2,1,2); % رسم الشكل الثاني في الأسفل
stairs(x, cdf, 'LineWidth', 2, 'Color', 'r');
title('دالة التوزيع التراكمي (CDF)');
xlabel('وجه النرد');
ylabel('الاحتمال التراكمي F(X)');
grid on;
