% 1. محاكاة رمي النرد 10 مرات
X = randi([1 6], 1, 10); 

% 2. تحويل النتائج إلى إشارة +1 و -1 
binary_signal = (-1).^mod(X, 2);
%{
mod(X, 2): هذه الدالة تحسب "باقي القسمة على 2".
​إ        ذا كان الرقم زوجياً (مثل 2, 4, 6)، يكون الناتج 0
            ​إذا كان الرقم فردياً (مثل 1, 3, 5)، يكون الناتج 1.
           ​(-1).^: هنا نقوم برفع الرقم (-1) للأس الذي نتج من الخطوة السابقة:
       ​إذا كان الرقم زوجياً: تصبح المعادلة (-1)^0 = 1.
​إ         ذا كان الرقم فردياً: تصبح المعادلة (-1)^1 = -1.

%}
% 3. الرسم البياني
figure; % فتح نافذة رسم جديدة
stem(binary_signal, 'LineWidth', 2, 'MarkerFaceColor', 'red'); % رسم الإشارة بنقاط
ylim([-1.5 1.5]); % ضبط حدود المحور الرأسي لتكون الإشارة واضحة
grid on; % إضافة شبكة للرسم

% إضافة تسميات
xlabel('رمية النرد (رقم الرمية)');
ylabel('قيمة الإشارة (+1 أو -1)');
title('تمثيل نتائج النرد كإشارة ثنائية (فردي = -1، زوجي = +1)');
