clc; clear; close all;
% clc       : مسح نافذة الأوامر
% clear     : حذف جميع المتغيرات من الذاكرة
% close all : إغلاق جميع النوافذ الرسومية المفتوحة

%% إعدادات النظام

% عدد البتات المراد إرسالها في التجربة
n = 1000;

% سعة الإشارة المستخدمة للتمثيل
% سنستخدم +1 لتمثيل البت 1 و -1 لتمثيل البت 0
A = 1;

% الانحراف المعياري للضوضاء Gaussian
% كلما زادت sigma زاد تأثير التشويش
sigma = 1;

%% توليد البتات عشوائياً

% إنشاء متجه يحتوي على n بتات عشوائية (0 أو 1)
data = randi([0 1],1,n);

%% Mapping (تحويل البتات إلى إشارات)

% تحويل البتات الثنائية إلى مستويات إرسال:
% عندما data = 0  يصبح x = -1
% عندما data = 1  يصبح x = +1
%
% المعادلة:
% x = 2*data - 1
x = 2*data - 1;

%% توليد الضوضاء Gaussian

% randn يولد عينات من توزيع طبيعي بمتوسط صفر وانحراف معياري 1
% نضرب في sigma للتحكم بقوة الضوضاء
z = sigma * randn(1,n);

%% تكوين الإشارة المستقبلة

% القناة تضيف الضوضاء إلى الإشارة الأصلية
% y هي الإشارة التي يستقبلها المستقبل
y = x + z;

%% اتخاذ القرار في المستقبل

% قاعدة القرار:
% إذا كانت القيمة المستقبلة أكبر من صفر => نقرر أنها 1
% إذا كانت أصغر أو تساوي صفر => نقرر أنها 0
decision = y > 0;

%% حساب عدد الأخطاء

% نقارن البتات الأصلية مع القرارات المستقبلة
% ~= تعني "غير مساوي"
% إذا اختلفت القيمة فهذا خطأ
errors = sum(decision ~= data);

%% حساب معدل خطأ البتات BER

% BER = عدد الأخطاء / إجمالي عدد البتات
BER = errors / n;

%% طباعة النتائج

fprintf('عدد الأخطاء = %d\n', errors);
fprintf('BER = %.4f\n', BER);

%% رسم أول 30 عينة من الإشارة المرسلة

figure;
stem(x(1:30),'filled');
% رسم قيم أول 30 رمز مرسل على شكل أعمدة منفصلة

title('Transmitted Signal');
xlabel('Sample Index');
ylabel('Amplitude');
grid on;

%% رسم أول 30 عينة من الإشارة المستقبلة

figure;
stem(y(1:30),'filled');
% رسم أول 30 قيمة بعد إضافة الضوضاء
% ستلاحظ أن القيم لم تعد +1 و -1 فقط

title('Received Signal');
xlabel('Sample Index');
ylabel('Amplitude');
grid on;


%% =========================================================
% الجزء الأول : تحليل الضوضاء Gaussian Noise
%% =========================================================

figure;
% فتح نافذة رسم جديدة

plot(z(1:100),'LineWidth',1.5);
% رسم أول 100 عينة من الضوضاء z
% الهدف: مشاهدة كيف تتغير الضوضاء عشوائياً مع الزمن/الفهرس

grid on;
% إظهار الشبكة لتسهيل القراءة

title('Gaussian Noise Samples');
xlabel('Sample Index');
ylabel('Amplitude');

%% ---------------------------------------------------------

figure;
% نافذة رسم جديدة

histogram(z,50,'Normalization','pdf');
% رسم Histogram للضوضاء
% 50 صندوق (bins)
% Normalization = pdf
% لجعل المدرج التكراري يمثل كثافة احتمالية

grid on;

title('Histogram of Noise');
xlabel('z');
ylabel('Probability Density');

%% ---------------------------------------------------------

figure;

histogram(z,50,'Normalization','pdf');
% إعادة رسم Histogram حتى نقارنه مع المنحنى النظري

hold on;
% إبقاء الرسم الحالي وإضافة رسم آخر فوقه

% إنشاء محور أفقي من -4σ إلى +4σ
t = linspace(-4*sigma,4*sigma,1000);

% حساب الدالة النظرية للتوزيع الطبيعي Gaussian PDF
pdf = (1/(sigma*sqrt(2*pi))) * exp(-(t.^2)/(2*sigma^2));

plot(t,pdf,'r','LineWidth',2);
% رسم المنحنى النظري باللون الأحمر

grid on;

title('Histogram vs Theoretical PDF');
legend('Histogram','Theoretical PDF');

%% =========================================================
% الجزء الثاني : حساب BER عند قيم مختلفة من SNR
%% =========================================================

%c; clear; close all;
% تنظيف البيئة من جديد

%% إعدادات المحاكاة

n = 1e5;
% عدد كبير من البتات = 100000
% نستخدم عدد كبير للحصول على BER أدق

A = 1;
% سعة الإشارة

snr_dB = 0:1:10;
% قيم SNR من 0 dB إلى 10 dB بخطوة 1 dB

BER = zeros(size(snr_dB));
% تهيئة مصفوفة لتخزين BER لكل قيمة SNR

%% حلقة المحاكاة

for k = 1:length(snr_dB)

    % تحويل SNR من dB إلى قيمة خطية
    SNR = 10^(snr_dB(k)/10);

    % حساب sigma من قيمة SNR
    % كلما زاد SNR تقل الضوضاء
    sigma = sqrt(A^2/(2*SNR));

    %% توليد البيانات

    data = randi([0 1],1,n);
    % بتات عشوائية

    %% Mapping

    x = 2*data - 1;
    % 0 -> -1
    % 1 -> +1

    %% إضافة الضوضاء

    z = sigma*randn(1,n);
    % ضوضاء Gaussian

    y = x + z;
    % الإشارة المستقبلة

    %% اتخاذ القرار

    decision = y > 0;
    % threshold = 0

    %% حساب BER

    BER(k) = sum(decision ~= data)/n;
    % عدد الأخطاء / عدد البتات

end

%% =========================================================
% رسم BER مقابل SNR
%% =========================================================

semilogy(snr_dB,BER,'o-','LineWidth',2);
% semilogy:
% المحور الأفقي عادي
% المحور الرأسي لوغاريتمي
% لأن BER يكون صغير جداً

grid on;

xlabel('SNR (dB)');
ylabel('BER');
title('BER vs SNR');