
%{
n = 1000;
p = 0.1; % احتمالية الخطأ في القناة

% 1. توليد البيانات (المرسل)
x = rand(1, n) < 0.5;% هذا متغير Bernoulli 

% 2. توليد الضوضاء (القناة) بناءً على p
z = rand(1, n) < p;

% 3. تطبيق القناة (XOR) يدويًا
y = zeros(1, n);
for i = 1:n
    if x(i) == z(i)%حدث خطأ اذا 
        y(i) = 0;
    else
        y(i) = 1;
    end
end

% 4. حساب BER (نسبة الخطأ)
error_count = 0;
for i = 1:n
    if x(i) ~= y(i)
        error_count = error_count + 1;
    end
end
calculated_ber = error_count / n;

fprintf('الـ BER المحسوب هو: %f (يجب أن يكون قريباً من p)\n', calculated_ber);



% --- الجزء الأول: المحاكاة (كما فعلت أنت) ---
N = 1000;
p = 0.3; 
X = rand(1, N) < 0.5;
Z = rand(1, N) < p; 
Y = mod(X + Z, 2); 

% --- الجزء الثاني: حساب الاحتمالات (القرار) ---
% نريد معرفة: إذا استلمنا 1، هل الأرجح أنه كان 0 أم 1؟


sent0_and_received1 = sum(X == 0 & Y == 1 );
%عدد المرات التي استلمنا فيها 1 وكان المرسل 0 وهذا مرات الخطا 
sent1_and_received1 = sum(X == 1 & Y == 1);
% عدد المرات التي استلمنا 1 وكان المرسل هو 1،وهذا استلام صحيح 

% --- الجزء الثالث: اتخاذ القرار (X_hat) ---
% سنبني متجهاً جديداً للقرار
X_hat = zeros(1, N);
for i = 1:N
    if Y(i) == 1 % اذا استلمنا 1 نقارن لكي نقرر هل هو 0 او 1 
        if sent1_and_received1 > sent0_and_received1
            % اذا عدد مرات التي استلمنا 1 وكان المرسل 1 بالفعل اكبر من عدد مرات استلام 1 والمرسل 0 
            %القرار 
            X_hat(i) = 1 ;% نقرر  ان القيمة المرسله هي 1
        else
            X_hat(i) = 0; 
        end
    else
        % اذا كان المستلم صفر ماذا نقرر بشان القيمه المرسلة بالفعل 
        % نفس المنطق للصفر
        if sum(X==0 & Y==0) > sum(X==1 & Y==0)
            X_hat(i) = 0;
        else
            X_hat(i) = 1;
        end
    end
end

% --- الجزء الرابع: BER النهائي ---
final_error = sum(X ~= X_hat); % كم مرة اخطأنا وقررنا ان القيمة المرسلة قيمة معنة وكان قررانا خاطئ 
BER_final = final_error / N; 

disp(['عدد الأخطاء بعد القرار: ', num2str(final_error)])
disp(['نسبة الخطأ النهائية: ', num2str(BER_final)])

