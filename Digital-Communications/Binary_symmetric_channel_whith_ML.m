% ===============================
% Binary Symmetric Channel + ML
% ===============================

clc; clear;

% --- الإعدادات ---
N = 10000;     % عدد البتات (كبير لنتائج أدق)
p = 0.3;       % احتمال الخطأ

% --- توليد المصدر ---
X = rand(1, N) < 0.5;   % Bern(0.5)

% --- توليد الضوضاء ---
Z = rand(1, N) < p;     % Bern(p)

% --- القناة (BSC) ---
Y = mod(X + Z, 2);      % XOR

% ===============================
% 1) BER قبل أي قرار (Raw)
% ===============================
BER_raw = sum(X ~= Y) / N;

% ===============================
% 2) قرار ML
% ===============================
% في BSC:
% إذا p < 0.5 → X_hat = Y
% إذا p > 0.5 → X_hat = 1 - Y

if p < 0.5
    X_hat = Y;
else
    X_hat = 1 - Y;
end

% ===============================
% 3) BER بعد ML
% ===============================
BER_ML = sum(X ~= X_hat) / N;

% ===============================
% 4) تحقق إحصائي
% ===============================
P_Y1 = mean(Y);

% ===============================
% عرض النتائج
% ===============================
fprintf('BER قبل القرار  = %.4f (≈ p)\n', BER_raw);
fprintf('BER بعد ML      = %.4f\n', BER_ML);
fprintf('P(Y=1)          = %.4f (≈ 0.5)\n', P_Y1);
