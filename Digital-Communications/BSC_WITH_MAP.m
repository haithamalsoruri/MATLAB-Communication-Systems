%% ================================
% 8. رسم النتائج
% ================================

figure('Color','w');
plot(p_error_range, BER,'b-o','LineWidth',1.5);
grid on;

xlabel('احتمال خطأ القناة p');
ylabel('BER (نسبة خطأ البت)');
title('أداء MAP Detector على قناة BSC');

